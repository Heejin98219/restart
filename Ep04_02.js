// <ol> (Ordered List)
// 순서가 있는 목록 표현

// <ul> (Unordered List)
// 순서가 없는 목록 표현
// type 속성으로 글머리 기호 변경 가능

// <li> (Listed Item)
// 목록하위 항목
// <ol> 또는 <ul> 태그의 하위에 위치

// <dl> (Definition Item: 정의 목록)
// 용어를 설명하는 목록을 만듦
// ex) A는 B이다. 와 같은 Key = Value로 사용할 때 유용

// <dt> (Definition Term: 정의 용어)
// 정의되는 용어의 제목을 넣을 때 사용

// <dd> (Definition Description: 정의 설명)
// 용어를 설명하는 데 사용

// <dl>태그는 하나 이상의 <dt>-<dd>쌍의 태그를 가져야 함
// <dt>-<dd>태그가 반드시 하나의 짝으로 지어져야 하는 것은 아님
// <li>, <dt>-<dd> 태그는 밖에서 독립적으로 사용 불가

// <ul>태그 하위 요소로 <li>태그가 위치해야 함
