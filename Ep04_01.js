// <h1> ~ <h6> (Heading)
// 웹 페이지의 제목 또는 부제목을 표현
// 숫자가 작을 수록 큰 제목을 표시

// <p> (Paragraph)
// 하나의 문단 표시

// <hr> (Horizontal Rule)
// 가로로 선을 긋는 태그
// 종료 태그 없음

// <br/> (Break)
// 줄바꿈 태그
// HTML에서의 개행 역할
// 종료태그 없음

// <i> (Italic)
// 텍스트를 이텔릭체로 표시

// <em> (Emphasis)
// 텍스트를 이텔릭체로 강조

// <b> (bold)
// 텍스트를 진하게 표시

// <strong>
// 텍스트 진하게 강조

// <b>태그와 <i>태그
// 텍스트를 진하고 이텔릭체로 표시

// <strong>태그와 <em>태그
// 실제로 중요한 부분 강조
// 용도에 맞게 사용하면 웹 접근성에 큰 기여
